package Collections.ListInterface;

public class ListInterfaceDescription {
    /*
        List: This is the child interface of the Collection interface.This interface is dedicated to the data of the list type in which we can store all the ordered collection of the objects.
        This also allows duplicate data to be present in it. This list interface is implemented by various classes like ArrayList, LinkedList Vector etc.
        Since all the subclasses implement the list, we can instantiate a list object with any of these classes. For example,

        List<T> al = new ArrayList<>();
        List<T> ll = new LinkedList<>();
        List<T> ve = new Vector<>();
        Where T is the type of the object.
     */
}
